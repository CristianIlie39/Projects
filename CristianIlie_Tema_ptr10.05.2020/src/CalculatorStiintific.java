public class CalculatorStiintific extends Calculator {

    public CalculatorStiintific() {

    }

    public double ridicareLaPutere(int x, int y) {
        return Math.pow(x, y);
    }

    public double radical(int x) {
        return Math.sqrt(x);
    }

    public double logaritmBaza10(int x) {
        return Math.log10(x);
    }

    public double logaritm(int x) {
        return Math.log(x);
    }

}
